 class Arithmetic {
    public static void main(String[] args) {
        int integerNumber = 500 ;
        double dubs  = 100d;
        float floatNumber = 25.50f;
    
        double result1 = integerNumber+dubs+floatNumber;
        double result2 = integerNumber-dubs-floatNumber;
        double result3= integerNumber*dubs*floatNumber;

        System.out.println("Result is :"+result1);
        System.out.println("Result is :"+result2);
        System.out.println("Result is :"+result3);
    }
    
}
